package exchangevaluesexercise;

public class ExchangeValuesExercise
{

    public static void main(String[] args)
    {
        int a;
        int b;
        int c;
        
        a = 7;
        b = 5;
        
        c = a;
        a = b;
        b = c;
        
        System.out.println(b);
    }

}
